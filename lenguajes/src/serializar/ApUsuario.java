    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombre=textoNombre.getText();
        String email=textoEmail.getText();
        int edad=Integer.parseInt(textoEdad.getText());
        
        //con la ayuda de la clase persistencia usuario guardamos a este ususario
        
        Usuario u=new Usuario(nombre,email,edad);
        //con la ayuda de la clase persistencia usuario ahora si guardamos este objeto
        PersistenciaUsuario p=new PersistenciaUsuario();
        
        try{
            p.guardar(u);
            JOptionPane.showConfirmDialog(this, "ususario guardado");
            
        }catch (Exception ex) {
            
            JOptionPane.showConfirmDialog(this, ex.getMessage());
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

